import { useState, useCallback, useEffect } from 'react';

interface CrudService<T, F> {
  getAll: () => Promise<T[]>;
  getById: (id: number) => Promise<T>;
  create: (data: F) => Promise<T>;
  update: (id: number, data: F) => Promise<T>;
  delete: (id: number) => Promise<void>;
  search?: (term: string) => Promise<T[]>;
}

interface CrudOptions<T, F> {
  entityName: string;
  service: CrudService<T, F>;
  cacheKey: string;
  getItemId: (item: T) => number | undefined;
  validateData?: (data: T[]) => boolean;
}

export function useCrudEntity<T, F>({
  entityName,
  service,
  cacheKey,
  getItemId,
  validateData
}: CrudOptions<T, F>) {
  // Estados comunes
  const [items, setItems] = useState<T[]>([]);
  const [selectedItem, setSelectedItem] = useState<T | null>(null);
  const [isEditMode, setIsEditMode] = useState(false);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [isOfflineMode, setIsOfflineMode] = useState(false);
  const [searchTerm, setSearchTerm] = useState('');
  const [lastSyncTime, setLastSyncTime] = useState<Date | null>(null);

  // üî• CARGAR DATOS CON MEJOR MANEJO DE ERRORES
  const loadItems = useCallback(async () => {
    try {
      setLoading(true);
      setError(null);
      
      console.log(`üîÑ [${entityName}] Cargando datos...`);
      
      try {
        const data = await service.getAll();
        
        // üî• VALIDACI√ìN MEJORADA DE DATOS
        if (!Array.isArray(data)) {
          console.warn(`‚ö†Ô∏è [${entityName}] Los datos recibidos no son un array:`, data);
          throw new Error('Los datos recibidos no son v√°lidos');
        }
        
        if (validateData && !validateData(data)) {
          console.warn(`‚ö†Ô∏è [${entityName}] Los datos no pasaron la validaci√≥n`);
          throw new Error('Los datos recibidos no son v√°lidos');
        }
        
        setItems(data);
        setIsOfflineMode(false);
        setLastSyncTime(new Date());
        
        // Guardar en cach√© de forma segura
        try {
          localStorage.setItem(cacheKey, JSON.stringify(data));
        } catch (cacheError) {
          console.warn(`‚ö†Ô∏è [${entityName}] Error al guardar en cach√©:`, cacheError);
        }
        
        console.log(`‚úÖ [${entityName}] ${data.length} elementos cargados`);
        
      } catch (apiError: any) {
        console.error(`‚ùå [${entityName}] Error al cargar desde API:`, apiError);
        
        // üî• INTENTAR CARGAR DESDE CACH√â CON MEJOR MANEJO
        try {
          const cached = localStorage.getItem(cacheKey);
          if (cached && cached !== 'null' && cached !== 'undefined') {
            const cachedData = JSON.parse(cached);
            
            if (Array.isArray(cachedData)) {
              setItems(cachedData);
              setIsOfflineMode(true);
              console.log(`üì¶ [${entityName}] ${cachedData.length} elementos cargados desde cach√©`);
            } else {
              throw new Error('Datos de cach√© no v√°lidos');
            }
          } else {
            throw new Error('No hay datos en cach√©');
          }
        } catch (cacheError) {
          console.error(`‚ùå [${entityName}] Error al cargar desde cach√©:`, cacheError);
          setItems([]);
          setIsOfflineMode(true);
          throw apiError;
        }
      }
    } catch (err: any) {
      console.error(`‚ùå [${entityName}] Error general:`, err);
      setError(err.message || `Error al cargar ${entityName}`);
      
      // üî• NO LIMPIAR ITEMS SI YA TENEMOS DATOS
      if (items.length === 0) {
        setItems([]);
      }
    } finally {
      setLoading(false);
    }
  }, [entityName, service, cacheKey, validateData, items.length]);

  // üî• BUSCAR ELEMENTOS CON MEJOR MANEJO
  const searchItems = useCallback(async (term: string) => {
    setSearchTerm(term);
    
    if (!term || !term.trim()) {
      console.log(`üîç [${entityName}] T√©rmino de b√∫squeda vac√≠o, recargando todos los elementos`);
      await loadItems();
      return;
    }
    
    setLoading(true);
    setError(null);
    
    try {
      console.log(`üîç [${entityName}] Buscando: "${term}"`);
      
      if (service.search && !isOfflineMode) {
        try {
          const results = await service.search(term);
          if (Array.isArray(results)) {
            setItems(results);
            console.log(`‚úÖ [${entityName}] ${results.length} resultados encontrados`);
          } else {
            throw new Error('Resultados de b√∫squeda no v√°lidos');
          }
        } catch (searchError) {
          console.warn(`‚ö†Ô∏è [${entityName}] Error en b√∫squeda API, usando b√∫squeda local`);
          // Fallback a b√∫squeda local
          const allItems = await service.getAll();
          const termLower = term.toLowerCase();
          const filtered = allItems.filter(item => {
            try {
              return JSON.stringify(item).toLowerCase().includes(termLower);
            } catch {
              return false;
            }
          });
          setItems(filtered);
        }
      } else {
        // B√∫squeda local
        console.log(`üîç [${entityName}] Realizando b√∫squeda local`);
        const termLower = term.toLowerCase();
        
        // Si no tenemos items, cargar primero
        let searchItems = items;
        if (searchItems.length === 0) {
          try {
            searchItems = await service.getAll();
          } catch {
            console.warn(`‚ö†Ô∏è [${entityName}] No se pudieron cargar elementos para b√∫squeda`);
            return;
          }
        }
        
        const filtered = searchItems.filter(item => {
          try {
            return JSON.stringify(item).toLowerCase().includes(termLower);
          } catch {
            return false;
          }
        });
        
        setItems(filtered);
        console.log(`‚úÖ [${entityName}] ${filtered.length} elementos filtrados localmente`);
      }
    } catch (error: any) {
      console.error(`‚ùå [${entityName}] Error al buscar:`, error);
      setError(`Error al buscar: ${error.message}`);
    } finally {
      setLoading(false);
    }
  }, [entityName, service, isOfflineMode, loadItems, items]);

  // üî• SELECCIONAR ELEMENTO CON VALIDACI√ìN
  const selectItem = useCallback((item: T) => {
    if (!item) {
      console.warn(`‚ö†Ô∏è [${entityName}] Intento de seleccionar elemento null/undefined`);
      return;
    }
    
    console.log(`üéØ [${entityName}] Elemento seleccionado:`, item);
    setSelectedItem(item);
    setIsEditMode(true);
  }, [entityName]);

  // Limpiar selecci√≥n
  const clearSelection = useCallback(() => {
    console.log(`üßπ [${entityName}] Limpiando selecci√≥n`);
    setSelectedItem(null);
    setIsEditMode(false);
  }, [entityName]);

  // üî• GUARDAR ELEMENTO CON MEJOR MANEJO
  const saveItem = useCallback(async (data: F): Promise<T | null> => {
    if (!data) {
      console.error(`‚ùå [${entityName}] No se pueden guardar datos null/undefined`);
      throw new Error('Datos no v√°lidos para guardar');
    }
    
    try {
      setLoading(true);
      setError(null);
      
      console.log(`üíæ [${entityName}] Guardando:`, data);
      
      let result: T;
      
      if (isEditMode && selectedItem) {
        const id = getItemId(selectedItem);
        if (id === undefined || id === null) {
          throw new Error('ID no v√°lido para actualizar');
        }
        
        console.log(`üìù [${entityName}] Actualizando ID ${id}`);
        result = await service.update(id, data);
        
        // Actualizar en la lista
        setItems(prev => prev.map(item => 
          getItemId(item) === id ? result : item
        ));
      } else {
        console.log(`‚ûï [${entityName}] Creando nuevo`);
        result = await service.create(data);
        
        // A√±adir a la lista
        setItems(prev => [...prev, result]);
      }
      
      clearSelection();
      setLastSyncTime(new Date());
      
      // üî• ACTUALIZAR CACH√â DE FORMA SEGURA
      try {
        const currentItems = await service.getAll();
        localStorage.setItem(cacheKey, JSON.stringify(currentItems));
      } catch (cacheError) {
        console.warn(`‚ö†Ô∏è [${entityName}] Error al actualizar cach√©:`, cacheError);
      }
      
      console.log(`‚úÖ [${entityName}] Guardado exitosamente`);
      return result;
      
    } catch (err: any) {
      console.error(`‚ùå [${entityName}] Error al guardar:`, err);
      setError(err.message || `Error al guardar ${entityName}`);
      throw err;
    } finally {
      setLoading(false);
    }
  }, [entityName, isEditMode, selectedItem, service, getItemId, cacheKey, clearSelection]);

  // üî• ELIMINAR ELEMENTO CON VALIDACI√ìN
  const deleteItem = useCallback(async (id: number) => {
    if (!id || isNaN(id)) {
      console.error(`‚ùå [${entityName}] ID no v√°lido para eliminar:`, id);
      throw new Error('ID no v√°lido para eliminar');
    }
    
    try {
      setLoading(true);
      setError(null);
      
      console.log(`üóëÔ∏è [${entityName}] Eliminando ID:`, id);
      
      await service.delete(id);
      
      // Remover de la lista
      setItems(prev => prev.filter(item => getItemId(item) !== id));
      
      // Limpiar selecci√≥n si es el elemento eliminado
      if (selectedItem && getItemId(selectedItem) === id) {
        clearSelection();
      }
      
      // üî• ACTUALIZAR CACH√â
      try {
        const updatedItems = items.filter(item => getItemId(item) !== id);
        localStorage.setItem(cacheKey, JSON.stringify(updatedItems));
      } catch (cacheError) {
        console.warn(`‚ö†Ô∏è [${entityName}] Error al actualizar cach√© despu√©s de eliminar:`, cacheError);
      }
      
      console.log(`‚úÖ [${entityName}] Eliminado exitosamente`);
      
    } catch (err: any) {
      console.error(`‚ùå [${entityName}] Error al eliminar:`, err);
      setError(err.message || `Error al eliminar ${entityName}`);
      throw err;
    } finally {
      setLoading(false);
    }
  }, [entityName, service, items, selectedItem, getItemId, cacheKey, clearSelection]);

  // Recargar datos
  const refreshItems = useCallback(async () => {
    console.log(`üîÑ [${entityName}] Refrescando datos...`);
    setSearchTerm('');
    await loadItems();
  }, [entityName, loadItems]);

  // üî• CARGAR DATOS INICIALES SOLO UNA VEZ
  useEffect(() => {
    let mounted = true;
    
    const loadInitialData = async () => {
      if (mounted && items.length === 0 && !loading && !error) {
        console.log(`üöÄ [${entityName}] Carga inicial de datos`);
        await loadItems();
      }
    };
    
    loadInitialData();
    
    return () => {
      mounted = false;
    };
  }, []); // Solo ejecutar una vez al montar

  return {
    // Estados
    items,
    selectedItem,
    isEditMode,
    loading,
    error,
    isOfflineMode,
    searchTerm,
    lastSyncTime,
    
    // Funciones
    loadItems,
    searchItems,
    selectItem,
    clearSelection,
    saveItem,
    deleteItem,
    setIsEditMode,
    refreshItems,
    setError
  };
}